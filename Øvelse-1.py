# Øvelse 1. Typecasting
# Lav typecasting fra floatingpoints(decimaltal) til integer(heltal)

# Vi definerer vores variabel med et tilfældigt decimaltal(floatingpoint)
float1 = 1.23

# Vi laver en variabel der omdanner og lagrer vores integer(heltal) som et decimaltal(floatingpoint)
float_p = int(float1)

# Vi printer typen af vores nye heltalsvariabel og variablen.
print(float_p ,type(float_p))