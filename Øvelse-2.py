# Øvelse 2. Typecasting
# Lav typecasting af heltal til decimaltal

# Vi definerer vores variabel med et tilfældigt heltal(integer)
heltal = 10

# Vi laver en variabel der omdanner og lagrer vores heltal(integer) som et decimaltal (floatingpoint)
decimaltal = float(heltal)

# Vi printer typen af vores nye decimaltalsvariabel og variablen.
print (decimaltal)